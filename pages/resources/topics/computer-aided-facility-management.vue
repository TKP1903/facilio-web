        var position = window.pageYOffset;
        var guideViewTop = $(window).scrollTop();
        var guideViewBottom = guideViewTop + $(window).height();
        var elemTop = $("#fc-footer").offset().top;
        if (position > 500 && !(guideViewBottom >= elemTop)) {
          $("#seo-sidebar").css({
            position: "fixed",
            top: "0",
            height: "auto",
            "margin-top": "100px",
            width: "100%"
          });
        } else {
          if (guideViewBottom >= elemTop) {
            $("#seo-sidebar").css({
              top: 0 - (guideViewBottom - elemTop)
            }); // scroll the #main div relative to the footer
          } else {
            $("#seo-sidebar").css({
              position: "relative",
              top: "auto",
              "margin-top": "0px",
              width: "100%"
            });
          }
        }
      }
    };
    $(document).ready(function() {
      $("a[href*=#]").bind("click", function(e) {
        e.preventDefault(); // prevent hard jump, the default behavior

        var target = $(this).attr("href"); // Set the target as variable

        // perform animated scrolling by getting top-position of target-element and set it as scroll target
        $("html, body")
          .stop()
          .animate(
            {
              scrollTop: $(target).offset().top
            },
            800,
            function() {
              location.hash = target; //attach the hash (#jumptarget) to the pageurl
            }
          );

        return false;
      });
    });

